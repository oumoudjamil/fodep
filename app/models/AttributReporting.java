package models;

public class AttributReporting {
    private String codeAttribut;
    private String libelleAttribut;
    private String sourceValeur;
    private String sourceDonnees;

    public String getCodeAttribut() {
        return codeAttribut;
    }

    public void setCodeAttribut(String codeAttribut) {
        this.codeAttribut = codeAttribut;
    }

    public String getLibelleAttribut() {
        return libelleAttribut;
    }

    public void setLibelleAttribut(String libelleAttribut) {
        this.libelleAttribut = libelleAttribut;
    }

    public String getSourceValeur() {
        return sourceValeur;
    }

    public void setSourceValeur(String sourceValeur) {
        this.sourceValeur = sourceValeur;
    }

    public String getSourceDonnees() {
        return sourceDonnees;
    }

    public void setSourceDonnees(String sourceDonnees) {
        this.sourceDonnees = sourceDonnees;
    }
}
